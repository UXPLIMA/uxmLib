package com.uxplima.uxmlib.gui;

/** The direction a {@link ScrollingGui} scrolls its content. */
public enum ScrollType {

    /** Scroll vertically: items fill rows left-to-right, and scrolling reveals later rows. */
    VERTICAL,

    /** Scroll horizontally: items fill columns top-to-bottom, and scrolling reveals later columns. */
    HORIZONTAL
}
